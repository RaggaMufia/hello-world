#include <stdio.h>

int		ft_isupper(int c)
{
	if (c >= 97 && c <= 112)
		return (c + ('A' - 'a'));
	else if (c >= 65 && c <= 90)
		return (c);
	else
		write(2, "Invalid character!\n", 19);
	return (0);
}
