int		ft_isspace(int c)
{
	if (c == ' ' || c == '\t' || c == '\v' || c == '\f' || c == '\r' || c == '\n')
		return (c);
	else if (c != ' ' || c != '\t' || c != '\v' || c != '\f' || c != '\r' || c != '\n')
		return (0);
	else
		return (0);
}
